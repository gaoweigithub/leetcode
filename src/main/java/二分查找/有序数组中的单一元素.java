package 二分查找;

public class 有序数组中的单一元素 {

    public static void main(String[] args) {
        有序数组中的单一元素 dd= new 有序数组中的单一元素();

        System.out.println(dd.singleNonDuplicate(new int[]{}));
    }

    public int singleNonDuplicate(int[] nums) {
        if (nums.length == 1){
            return nums[0];
        }


    }
}
